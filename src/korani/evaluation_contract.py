"""Stage D assembly: Evaluator drafts the evaluation contract → evaluate.py.

The Evaluator LLM supplies judgment only (expected values, tolerances,
check kinds); evaluate.py itself is rendered deterministically here — the
script a human approves is exactly the script stage F runs, with no LLM
freedom in between.

The contract stays a DRAFT until a human approves it (non-negotiable
checkpoint, handled in the CLI via ``approve_contract``). Stage E must
refuse to run against a draft.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Dict, Optional, Tuple

from korani.agents.evaluator import Evaluator
from korani.llm import LLMClient, client_for_role
from korani.models import EvaluationContract, SimulationSpec
from korani.storage import Storage


class EvaluationContractError(RuntimeError):
    pass


def run_evaluation_contract(
    spec: SimulationSpec,
    work_id: str,
    config: Dict,
    client: Optional[LLMClient] = None,
) -> Tuple[EvaluationContract, str, str]:
    """Draft the contract and persist it. Returns (contract, script_path,
    contract_path). ``client`` injectable for tests."""
    if not spec.target_results:
        raise EvaluationContractError(
            "SimulationSpec has no target_results — nothing to verify a "
            "reproduction against. Review the spec file (extraction may have "
            "missed the results section) or pick a paper that reports "
            "concrete figures/tables."
        )
    evaluator_client, evaluator_model = client_for_role(config, "evaluator", client)
    evaluator = Evaluator(client=evaluator_client, model=evaluator_model)
    contract = evaluator.draft(spec)
    contract.work_id = work_id
    script_path, contract_path = persist_contract(contract, config)
    return contract, script_path, contract_path


def approve_contract(contract: EvaluationContract, config: Dict) -> Tuple[str, str]:
    """Lock the contract in after human review — the stage D checkpoint."""
    contract.status = "approved"
    return persist_contract(contract, config)


def persist_contract(contract: EvaluationContract, config: Dict) -> Tuple[str, str]:
    """DB row (history kept) + human-reviewable files under evaluations/."""
    data_dir = config.get("data_dir", "data")
    eval_dir = Path(data_dir) / "evaluations" / (contract.work_id or "unknown")
    eval_dir.mkdir(parents=True, exist_ok=True)

    script_path = eval_dir / "evaluate.py"
    contract_path = eval_dir / "contract.json"
    script_path.write_text(render_evaluate_py(contract), encoding="utf-8")
    contract_path.write_text(
        json.dumps(contract.model_dump(), indent=2, ensure_ascii=False),
        encoding="utf-8",
    )
    if contract.work_id:
        with Storage(data_dir) as storage:
            storage.save_contract(contract.work_id, contract)
    return str(script_path), str(contract_path)


# ── evaluate.py renderer ───────────────────────────────────────────
# The generated script is standalone (stdlib only): it reads the results
# file the stage E runner writes, verifies every numeric check within its
# tolerance, and lists curve/qualitative checks for the Result Analyst.
# Checks are embedded via repr() — always a valid, correctly escaped Python
# literal — and summarized in the docstring for the human reviewer.

_TEMPLATE = '''\
#!/usr/bin/env python3
"""Evaluation contract — generated by KORANI stage D. STATUS: {status}.

Paper : {title}
Solver: {solver}

Checks:
{summary}

The stage E runner must write computed values into {results_file!r}, keyed
by each check's "key". Numeric checks are verified here; curve/qualitative
checks are deferred to the stage F Result Analyst.

Usage: python evaluate.py [{results_file}]
Exit code: 0 = every numeric check passed, 1 = any failed or missing.
"""
import json
import sys

CHECKS = [
    {checks},
]


def main():
    path = sys.argv[1] if len(sys.argv) > 1 else {results_file!r}
    try:
        with open(path, "r", encoding="utf-8") as f:
            results = json.load(f)
    except (OSError, ValueError) as exc:
        print("cannot read results file %r: %s" % (path, exc))
        return 1

    n_pass = n_fail = n_deferred = 0
    for check in CHECKS:
        key = check["key"]
        if check["kind"] != "numeric" or check["expected_value"] is None:
            n_deferred += 1
            print("[ANALYST] %s: %s -- compare with %s"
                  % (key, check["description"], check["location"] or "the paper"))
            continue
        if key not in results:
            n_fail += 1
            print("[MISSING] %s: results file has no value for this check" % key)
            continue
        try:
            got = float(results[key])
        except (TypeError, ValueError):
            n_fail += 1
            print("[INVALID] %s: %r is not a number" % (key, results[key]))
            continue
        expected = float(check["expected_value"])
        tolerance = abs(expected) * check["rel_tol"]
        ok = abs(got - expected) <= tolerance if tolerance else got == expected
        if ok:
            n_pass += 1
        else:
            n_fail += 1
        print("[%s] %s: got %g, expected %g +/- %.1f%% (%s)"
              % ("PASS   " if ok else "FAIL   ", key, got, expected,
                 100 * check["rel_tol"], check["location"] or "?"))

    print("")
    print("numeric: %d passed, %d failed | deferred to Result Analyst: %d"
          % (n_pass, n_fail, n_deferred))
    return 1 if n_fail else 0


if __name__ == "__main__":
    sys.exit(main())
'''


def render_evaluate_py(contract: EvaluationContract) -> str:
    summary_lines = []
    for c in contract.checks:
        expected = c.expected_text or "(no tabulated value — analyst compares)"
        location = " [%s]" % c.location if c.location else ""
        summary_lines.append(
            "  - %s (%s)%s: %s -> %s" % (c.key, c.kind, location, c.description, expected)
        )
    checks_literal = ",\n    ".join(repr(c.model_dump()) for c in contract.checks)
    return _TEMPLATE.format(
        status=contract.status.upper(),
        title=contract.paper_title or "(unknown)",
        solver=contract.solver,
        summary="\n".join(summary_lines) or "  (none)",
        checks=checks_literal,
        results_file=contract.results_file,
    )
